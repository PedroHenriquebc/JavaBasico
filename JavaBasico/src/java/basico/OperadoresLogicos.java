package java.basico;

public class OperadoresLogicos {

	public static void main(String[] args) {
		int valor1 = 1;
		int valor2 = 2;
		System.out.println(valor1 == 1 && valor2 == 2);
		System.out.println(valor1 == 1 || valor2 == 2);
		System.out.println(valor1 == 1 ^ valor2 == 2);
	}
	/*
	   AND -> "&&" Só é verdadeiro se ambas afirmaçőes forem verdadeiras.
	   OR  -> "||" Só é falsa se ambas afirmaçőes forem falsas.
	   XOR -> "^"  Só é verdadeira quando as afirmaçőes forem diferentes.
	 
	*/
	

}
